/**
 * The purpose of this program is to define a player class for Prog3.java 
 * to build a circular linked list filled with player objects.
 */
public class Player {
private String name;
private int position;
public Player() {
	name = name;
	position = 0;
}
public Player(String name1, int position1) {
	name = name1;
	position = position1;
}
public void setName(String name2) {
	name = name2;
}
public void setPosition(int position2) {
	position = position2;
}
public String getName() {
	return name;
}
public int getPosition() {
	return position;
}
}
